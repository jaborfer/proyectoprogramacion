    /**
     * Este metodo busca en la tabla en el campo que le pasamos por parametro,
     * el texto que tambien le pas samos por parametro
     *
     * @param Campo String nombre del campo en el que vamos a buscar
     * @param Busca String texto que vamos a buscar
     */
    private void muestraTabla(String Campo, String Busca) {
        String consulta;
        String respuesta[] = new String[4];

        tablaComMat.setEnabled(true);
        tablaComMat.setVisible(true);
        DefaultTableModel tcm = new DefaultTableModel();
        tcm.addColumn("Pedido");
        tcm.addColumn("Cod. Manualidad");
        tcm.addColumn("Nombre");
        tcm.addColumn("Cantidad");
        tablaComMat.setModel(tcm);

        consulta = "select lineaspedido.PEDIDO_Npedido, lineaspedido.MANUALIDAD_codManualidad, manualidad.Nombre, lineaspedido.cantidad\n"
                + "from lineaspedido, manualidad\n"
                + "where  (manualidad.codManualidad=lineaspedido.MANUALIDAD_codManualidad) and (lineaspedido.PEDIDO_Npedido=" + claveCompoSalvada + ")";

        try {
            Statement st = cn.createStatement();
            ResultSet resultado = st.executeQuery(consulta);
            while (resultado.next()) {
                respuesta[0] = numeroPedido.getText();
                respuesta[1] = resultado.getString(2);
                respuesta[2] = resultado.getString(3);
                respuesta[3] = resultado.getString(4);

                tcm.addRow(respuesta);
            }// fin del while
        } catch (SQLException ex) {
            Logger.getLogger(ColoresForm.class.getName()).log(Level.SEVERE, null, ex);
        } //fin del try-catch

    } //fin del metod

