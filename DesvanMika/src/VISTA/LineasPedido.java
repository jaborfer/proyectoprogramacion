    /**
     * El boton guardar prepara el insert y lo ejecuta y refresca la tabla
     *
     *
     */
    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed

        try {
            PreparedStatement orden = cn.prepareStatement("INSERT INTO lineaspedido(pedido_npedido, manualidad_codmanualidad, cantidad) values(?,?,?)");
            orden.setInt(1, this.claveCompoSalvada);
            orden.setString(2, codManualidad.getText());
            orden.setString(3, cantidad.getText());
            orden.executeUpdate();
            JOptionPane.showMessageDialog(null, "Guardado con Exito");
            codManualidad.setText("");
            cantidad.setText("");
            muestraTabla("PEDIDO_Npedido", String.valueOf(claveCompoSalvada));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Se produjo un error, revise los datos");
            Logger.getLogger(LineasPedido.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_botonGuardarActionPerformed
    /**
     *
     * Esta opción es borrar, comprueba que hay una fila seleccionada, pregunta
     * si queremos borrar y si decimos que si borra prepara la orden delete
     */
    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        String orden;
        int fila;
        int respuesta;

        fila = tablaComMat.getSelectedRow();
        if (fila >= 0) {
            respuesta = JOptionPane.showConfirmDialog(null, "¿Seguro que que quiere borrar La manualidad " + tablaComMat.getValueAt(fila, 1) + " compuesto de " + tablaComMat.getValueAt(fila, 2) + "?");
            if (respuesta == 0) {
                orden = "DELETE FROM lineaspedido where PEDIDO_Npedido=" + claveCompoSalvada + " and COMPONENTE_codComponente=" + tablaComMat.getValueAt(fila, 1);
                PreparedStatement pst;
                try {
                    pst = cn.prepareStatement(orden);
                    pst.executeUpdate();
                } catch (SQLException ex) {
                    Logger.getLogger(ColoresForm.class.getName()).log(Level.SEVERE, null, ex);
                }//fin del try catch
                muestraTabla("PEDIDO_Npedido", String.valueOf(claveCompoSalvada));

            }// fin del if internos

        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fila con el boton izquierdo");
        } // fin del if exterior
    }//GEN-LAST:event_jMenuItem2ActionPerformed


