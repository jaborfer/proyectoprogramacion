    private void buscaTela() {
        String consulta;
        String respuesta[] = new String[5];
        consulta = "SELECT material.codMAterial, color.Colorprincipal, tela.Tejido,tela.Estampado, material.cantidad   \n"
                + "FROM tela , material, color\n"
                + "where tela.MATERIAL_codMaterial=material.codMaterial and material.COLOR_idCOLOR=color.idCOLOR";

        DefaultTableModel modeloTela = new DefaultTableModel();
        modeloTela.addColumn("NÂº Tela");
        modeloTela.addColumn("Color");
        modeloTela.addColumn("Tejido");
        modeloTela.addColumn("Estampado");
        modeloTela.addColumn("Cantidad");

        tablaTelas.setModel(modeloTela);
        try {
            Statement st = cn.createStatement();
            ResultSet resultado = st.executeQuery(consulta);
            while (resultado.next()) {
                respuesta[0] = resultado.getString(1);
                respuesta[1] = resultado.getString(2);
                respuesta[2] = resultado.getString(3);
                respuesta[3] = resultado.getString(4);
                respuesta[4] = resultado.getString(5);
                modeloTela.addRow(respuesta);
            } //Fin del while
            st.close();
            tablaTelas.setModel(modeloTela);
        } catch (SQLException ex) {
            Logger.getLogger(ClientesForm.class.getName()).log(Level.SEVERE, null, ex);
        } //fin del try-catch

    } // fin del metodo

